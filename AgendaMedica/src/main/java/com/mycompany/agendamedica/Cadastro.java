    private void bntLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntLoginActionPerformed
        // TODO add your handling code here:
        try{
        Gson gson = new GsonBuilder().setPrettyPrinting().create();
        String json= String.join(" ",Files.readAllLines(Paths.get("pacientes.json"),StandardCharsets.UTF_8));
        String json2= String.join(" ",Files.readAllLines(Paths.get("medicos.json"),StandardCharsets.UTF_8));
        java.lang.reflect.Type listTypePaciente = new TypeToken <ArrayList<Paciente>>(){}.getType();
        ArrayList<Paciente> pacientes = gson.fromJson(json,listTypePaciente);
        java.lang.reflect.Type listTypeMedico = new TypeToken <ArrayList<Medico>>(){}.getType();
        ArrayList<Medico> medicos = gson.fromJson(json2, listTypeMedico);
        //Caso login medico
        if(chxMedico.isSelected()==true&&chxPaciente.isSelected()==false){
            String nome = nomeTxt.getText().toUpperCase();
            String senha = senhaTxt.getText();
            boolean verificador = false;
            for(Medico medico : medicos){
                if(medico.getNome().equals(nome)&&medico.getSenha().equals(senha)){
                    verificador=true;
                    Writer writer = new FileWriter("usuario.json");
                    gson.toJson(medico,writer);
                    writer.flush();
                    writer.close();
                    PosLogin frame = new PosLogin();
                    
                    System.out.println("medico");

                    frame.setVisible(true);
                    //this.disable();
                    continue;
                }
           
            }
            if(verificador==false){
                JOptionPane.showMessageDialog(null, "Login Invalido, Cadastre-se ou cheque se o nome e senha estao corretos","ERRO", JOptionPane.ERROR_MESSAGE);
            }
            
        }
        //Caso login paciente
        if(chxMedico.isSelected()==false&&chxPaciente.isSelected()==true){
            String nome = nomeTxt.getText().toUpperCase();
            String senha = senhaTxt.getText();
            boolean verificador = false;
            for(Paciente paciente:pacientes){
                if(paciente.getNome().equals(nome)&&paciente.getSenha().equals(senha)){
                    verificador=true;
                    Writer writer = new FileWriter("usuario.json");
                    gson.toJson(paciente,writer);
                    writer.flush();
                    writer.close();
                    PosLogin frame = new PosLogin();
                    
                    System.out.println("paciente");
                    
                    frame.setVisible(true);
                    //this.disable();
                    continue;
                }
            }
            if(verificador==false){
                JOptionPane.showMessageDialog(null, "Login Invalido, Cadastre-se ou cheque se o nome e senha estao corretos","ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }
        //Caso erro
        if((chxMedico.isSelected()==true&&chxPaciente.isSelected()==true)||(chxMedico.isSelected()==false&&chxPaciente.isSelected()==false)){
            JOptionPane.showMessageDialog(null, "Ola, o Login nao deu certo! Voce deixou ambas opcoes ativas ou inativas","ERRO", JOptionPane.ERROR_MESSAGE);
        }
        }catch (IOException ex) {
            Logger.getLogger(CadastrarPessoa.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bntLoginActionPerformed

